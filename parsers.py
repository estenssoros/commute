def parse_accu_json(resp):
    data = {}
    data['apparent_temperature'] = resp['ApparentTemperature']['Imperial']['Value']
    data['ceiling'] = resp['Ceiling']['Imperial']['Value']
    data['cloud_cover'] = resp['CloudCover']
    data['dew_point'] = resp['DewPoint']['Imperial']['Value']
    data['is_day_time'] = resp['IsDayTime']
    data['past_24_hour_dep'] = resp['Past24HourTemperatureDeparture']['Imperial']['Value']
    data['1hr_precip'] = resp['Precip1hr']['Imperial']['Value']
    data['precip_12_hour'] = resp['PrecipitationSummary']['Past12Hours']['Imperial']['Value']
    data['precip_18_hour'] = resp['PrecipitationSummary']['Past18Hours']['Imperial']['Value']
    data['precip_24_hour'] = resp['PrecipitationSummary']['Past24Hours']['Imperial']['Value']
    data['precip_3_hour'] = resp['PrecipitationSummary']['Past3Hours']['Imperial']['Value']
    data['precip_6_hour'] = resp['PrecipitationSummary']['Past6Hours']['Imperial']['Value']
    data['precip_9_hour'] = resp['PrecipitationSummary']['Past9Hours']['Imperial']['Value']
    data['precip_1_hour'] = resp['PrecipitationSummary']['PastHour']['Imperial']['Value']
    data['precip'] = resp['PrecipitationSummary']['Precipitation']['Imperial']['Value']
    data['pressure'] = resp['Pressure']['Imperial']['Value']
    data['pressure_tendancy'] = resp['PressureTendency']['LocalizedText']
    data['temperature'] = resp['Temperature']['Imperial']['Value']
    data['temp_12_max'] = resp['TemperatureSummary']['Past12HourRange']['Maximum']['Imperial']['Value']
    data['temp_12_min'] = resp['TemperatureSummary']['Past12HourRange']['Minimum']['Imperial']['Value']
    data['temp_24_max'] = resp['TemperatureSummary']['Past24HourRange']['Maximum']['Imperial']['Value']
    data['temp_24_min'] = resp['TemperatureSummary']['Past24HourRange']['Minimum']['Imperial']['Value']
    data['temp_6_max'] = resp['TemperatureSummary']['Past6HourRange']['Maximum']['Imperial']['Value']
    data['temp_6_min'] = resp['TemperatureSummary']['Past6HourRange']['Minimum']['Imperial']['Value']
    data['uv_index'] = resp['UVIndex']
    data['visibility'] = resp['Visibility']['Imperial']['Value']
    data['weather'] = resp['WeatherText']
    data['wet_bulb_temp'] = resp['WetBulbTemperature']['Imperial']['Value']
    data['wind_direction'] = resp['Wind']['Direction']['Degrees']
    data['wind_speed'] = resp['Wind']['Speed']['Imperial']['Value']
    data['wind_chill'] = resp['WindChillTemperature']['Imperial']['Value']
    data['wind_gust'] = resp['WindGust']['Speed']['Imperial']['Value']
    return data
